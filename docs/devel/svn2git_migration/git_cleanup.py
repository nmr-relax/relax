for branch in ['1.3', 'cst_1.2.10', 'multi_processor', 'relax_disp_old', 'rdc_analysis_old']:
    exec_cmd("git checkout %s" % branch)
    exec_cmd("git reset --hard HEAD^")
exec_cmd("git checkout master")

