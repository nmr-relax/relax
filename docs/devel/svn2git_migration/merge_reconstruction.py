# Only execute the following if run as a script.
if __name__ == "__main__":
    # Testing mode.
    if TEST_COMMITS:
        # Go to the original repository.
        chdir(ORIG_REPO)

        # Get the hashes.
        hashes = get_hashes()

        # Checks.
        for merge_branch, merged_branch, merge_rev, merge_commit, rev_parent1, parent1, rev_parent2, parent2, merge_hash_index, parent1_hash_index, parent2_hash_index in ALL_MERGES:
            # Find the hashes.
            merge_commit_hash = find_hash(text=merge_commit, hashes=hashes, hash_index=merge_hash_index)
            if rev_parent1:
                parent1_hash = find_hash(text=parent1, hashes=hashes, hash_index=parent1_hash_index)
            if rev_parent2:
                parent2_hash = find_hash(text=parent2, hashes=hashes, hash_index=parent2_hash_index)

        # Find any unmerged commits.
        find_unmerged(merges=ALL_MERGES, false_pos=UNMERGED_FALSE_POS)

        # Stop the script.
        sys.exit()

    # Create a new repository.
    if not path.exists(NEW_REPO):
        exec_cmd("cp -rup %s %s" % (ORIG_REPO, NEW_REPO))
        chdir(NEW_REPO)

    # Reuse the old repository.
    else:
        chdir(NEW_REPO)

    # Parse the merge progress file and open it for writing.
    completed_merges = []
    if path.exists('.merge_reconstruction'):
        merged_file = open('.merge_reconstruction')
        completed_merges = merged_file.readlines()
        for i in range(len(completed_merges)):
            completed_merges[i] = completed_merges[i].strip()
    merged_file = open('.merge_reconstruction', 'a')

    # Fix the branch merge points.
    for merge_branch, merged_branch, merge_rev, merge_commit, rev_parent1, parent1, rev_parent2, parent2, merge_hash_index, parent1_hash_index, parent2_hash_index in ALL_MERGES:
        # Heading.
        print("\n\nMerge to branch \"%s\", from branch \"%s\"." % (merge_branch, merged_branch))

        # Generate the SHA1 hash for the merge and check if it exists.
        hash = sha1(("%s %s\n%s %s\n%s %s\n%s %s\n" % (merge_branch, merged_branch, merge_rev, merge_commit, rev_parent1, parent1, rev_parent2, parent2)).encode('utf8')).hexdigest()
        print("Unique merge hash: %s" % hash)
        if hash in completed_merges:
            print("Skipping: Merge already performed.")
            continue

        # Get the hashes (for each merge in case the history has been rewritten).
        hashes = get_hashes()

        # Find the hashes.
        merge_commit_hash = find_hash(text=merge_commit, hashes=hashes, hash_index=merge_hash_index)
        print("%-10s %-6s %s %s" % ("Commit:", merge_rev, merge_commit_hash, merge_commit))
        parent1_hash = None
        parent2_hash = None
        if rev_parent1:
            parent1_hash = find_hash(text=parent1, hashes=hashes, hash_index=parent1_hash_index)
            print("%-10s %-6s %s %s" % ("Parent 1:", rev_parent1, parent1_hash, parent1))
        if rev_parent2:
            parent2_hash = find_hash(text=parent2, hashes=hashes, hash_index=parent2_hash_index)
            print("%-10s %-6s %s %s" % ("Parent 2:", rev_parent2, parent2_hash, parent2))

        # No matches.
        if merge_commit_hash == None:
            print_error("The commit hash cannot be found.")
            continue

        # Fix up.
        filter = ""
        if parent1_hash:
            filter += "-p %s " % parent1_hash
        if parent2_hash:
            filter += "-p %s " % parent2_hash
        exec_cmd("git filter-branch --force --parent-filter 'test $GIT_COMMIT = %s && echo \"%s\" || cat' %s --all" % (merge_commit_hash, filter, merge_branch))

        # Clean up to prevent massive repository bloat.
        exec_cmd("git for-each-ref --format=\"%(refname)\" refs/original/ | xargs -n 1 git update-ref -d")
        exec_cmd("git reflog expire --expire=now --all")
        exec_cmd("git gc --prune=now")

        # Append the completed merge hash.
        merged_file.write("%s\n" % hash)
        merged_file.flush()
